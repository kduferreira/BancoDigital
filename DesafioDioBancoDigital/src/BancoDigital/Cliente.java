package BancoDigital;

public class Cliente {

	
	
	private String nome;
	
	private String CPF;
	
	private int idade;
	
	
	

	public Cliente(String nome) {
		this.nome = nome;
		this.CPF = CPF;
		this.idade = idade;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getCPF() {
		return CPF;
	}

	public void setCPF(String cPF) {
		CPF = cPF;
	}

	public int getIdade() {
		return idade;
	}

	public void setIdade(int idade) {
		this.idade = idade;
	}
	
	
	
	
}
